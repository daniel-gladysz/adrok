          <h3>Adrok’s results</h3>
          <p>Promising results regarding the identification of the ore body were encountered on several of Adrok’s interpretation tools. <br /><br />
          Distinct troughs in mean energy (E-Mean) of the returned ADR signal are possibly associated with the mineral zone. The difference in the broadness of the trough at each V-bore could indicate variation in sulphide body thickness. (Figure 2) <br /><br />
          Weighted Mean Frequency (WMF) is characterized by similar trend lines at a specific depth range, that is, a change from low values to high and again, to low. Peaks or lows in WMF logs at this depth range could be related with the presence of the ore body. (Figure 3) <br /><br />
          Very encouraging results came from the analysis of the transect scan. The transect image was created by combining together stationary scans (Stares) recorded at 25m intervals in a survey scan line of 500m. At each survey location the noise spectra was subtracted from the signal spectra. Then, the signals for each station were combined together to form the final image (Figure 4). As shown on Figure 4, the broad target area is characterized by low values in signal energy in contrast to the high values of the surrounding area. This leads to the conclusion that potentially the mineral body has been detected in signal energy.  <br /><br />
          The chosen areas of interest based on the mean energy (E-Mean) graphs were further analysed by conducting spectral lines analysis. <br /><br />
          At survey sites DP01-60w and DP06, highest portion of direct matches between extracted spectral lines and fundamental harmonics of known spectral lines of pyrite and pyrrhotite occur within the individual E-Mean troughs, approximately between the depths 100m and 150m. <br /><br />
          The detailed spectral line analysis from 50m to 250m on V-bore DP05-90 suggests one possible zone of interest for pyrite and pyrrhotite from 70m to 130m depth. On V-bore DP01-90 it is suggested a possible zone of interest for pyrrhotite at 120-140m depth and multiple zones for pyrite between 130m and 200m. However, spectral lines analysis was conducted beyond the zones of interest and showed that in V-bore DP05-90 there is an additional deeper zone of pyrrhotite at 300-400m depth and, in V-bore DP01-90 one deeper zone of pyrite at 200-300m and zones of pyrrhotite between 300m and 400m.
          </p>

