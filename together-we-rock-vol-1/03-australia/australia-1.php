          <h2>Atomic Dielectric Resonance</h2>
          <h2>DET-CRC Downhole Test Site</h2>

          <h3>Project aims</h3>
          <p>The principal aim of this project was to test Adrok’s ADR scanner against the drilled and well mapped sulphide body in the disused Brukunga mine site in Adelaide, South Australia. The ADR field survey was conducted in October 2012 on behalf of DET-CRC. DET-CRC is a research organisation involved with exploring new technologies in deep exploration, from drilling to new geophysical techniques as well as, finding safer ways to drill, analyse and target deep mineral deposits. Data for V-bores (virtual boreholes) as well as transect scans was collected. The individual objectives of this field survey were to identify the extent of sulphide body, to compare the ADR transect with the seismic survey transect and finally, to compare angled scans to vertical scans.</p>

          <h3>Exploration/Project challenges</h3>
          <p>The project challenge was to compare and match the results from the ADR survey with data of other geophysical techniques (e.g. seismic data), regarding the identification of the ore body.</p>

          <h3>Geological context</h3>
          <p>The Brukunga mine site, which was open between 1955 and 1972, is located in South Australia, 4km north of Nairne and 40km east of Adelaide in the Mount Lofty Ranges<sup>1</sup>. The mine was operated for the recovery of iron sulphide (pyrite and pyrrhotite), which was used for the production of sulphuric acid. <br /><br />
          Elemental sulphur occurs naturally in salt domes, as deposits around volcanoes and within sedimentary beds<sup>2</sup>. Apart from salt dome cap rock deposits, sulphur is also produced from pyrite (iron sulphide, FeS2) as in the case of the Brukunga mine site2. Sulphuric acid, the major derivative of sulphur, is the most important inorganic chemical used in commerce, being widely used in the chemical and fertiliser industries<sup>2</sup>. <br /><br />
          The ore body runs N-S, sub-vertical with slight dip to the east. The host rocks are Cambrian calc-siltstones (Nairne Pyrite member) lying within the north-south trending Kanmantoo Trough, the youngest sequence in the southern part of the Adelaide Geosynclin<sup>3</sup>. <br /><br />
          The Kanmantoo Trough formed as a rapidly subsiding, fault-controlled rift basin during the last of several phases of major crustal extension that occurred along the Early to Middle Cambrian palaeo-Pacific margin of Gondwana. The dominantly siliciclastic sediments deposited in the Kanmantoo Trough crop out along the eastern Mount Lofty Ranges, through Fleurieu Peninsula and on Kangaroo Island<sup>4</sup>. <br /><br />
          </p>

