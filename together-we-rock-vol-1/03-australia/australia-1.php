          <h3>Benefits for client</h3>
          <p>The ADR signal penetrated down to 1000m of solid rock and Adrok can presumably identify the broad mineral body by low energy values in signal energy. </p>

          <h3>Project outcomes</h3>
          <p>This project resulted in an important case study as many metallic minerals are associated with sulphide minerals and also, it was presented at November 2013 DET-CRC conference. At the conference the results were presented in a 3D model constructed by inverted magnetic isosurfaces and Google Earth Imagery, seismic data and ADR V-bores targeting the sulphide mineralisation (Figure 5).</p>

