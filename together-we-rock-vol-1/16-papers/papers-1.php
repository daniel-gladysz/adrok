            <h3>CONCLUSIONS</h3>
            <p>The results from experiments and commercial field surveys, as well as the numerical model simulation suggest that the exploration depth of pulsed radar can be increased significantly by including a low frequency component. The conductivity we found to be consistent with our data was less than 0.075mS/m translating in a skin depth of about 160m. <br /><br />
              If these results hold for other rock types, deeply penetrating radar scanning can potentially become an attractive geophysical exploration technique in selective environments where there is no highly conductive near-surface layer, or where this layer is thin enough to penetrate. We believe that these experiments are encouraging and warrant further investigations.</p>

