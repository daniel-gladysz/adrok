            <h2>1.  Introduction</h2>
            <p>The development of atomic dielectric resonance (ADR) by Dr G. Colin Stove was inspired by his experiences of working with synthetic aperture radar (SAR) sensors, from both satellites (ERS1 and RADARSAT) and from the Space Shuttle Imaging Radar-B experiment in the early 1980s, as well as his work with conventional ground penetrating radar (GPR) systems. ADR was developed as an improvement over SAR and GPR to achieve deeper penetration of the earth’s subsurface, through the creation and use of a novel type of coherent beam. ADR is used as a geophysical technique to provide a precision instrument for the accurate geological recognition of rock layers and identification of rock types by transmission through the rock medium as well as reflection from each subsurface rock layer. <br /><br />
              The aim of this poster presentation is to report on tests of the subsurface earth penetration capabilities of a new spectrometer as well as its ability to recognise thin coal layers and many sedimentary, igneous and metamorphic rock types in real world conditions.</p>
            <h2>2.  Methodology</h2>
            <h3>2.1 Description of the ADR System</h3>
            <p>ADR is a patented investigative technique (Stove 2005) that involves the measurement and interpretation of resonant energy responses of natural or synthetic materials to the interaction of pulsed electromagnetic radio waves or microwaves from materials which permit the applied energy to pass through the material. The resonant energy response can be measured in terms of energy, frequency, and phase relationships. The precision with which the process can be measured helps define the unique interactive atomic or molecular response behaviour of any specific material, according to the energy bandwidth used. ADR is measurable on a very wide range of hierarchical scales both in time and space. Time scales may range from seconds to femtoseconds, and spatial scales from metres to nanometres.</p> 
            <img src="images/coalbedmethane-1.jpg" />
            <span>Figure 1. The ADR scanner beam transmit and receive pathways through geological layers when one of the sensors is stationary and the other is moved progressively away at a regular speed.</span>
            <p>With reference to Figure 1, the wide-angle reflection and refraction (WARR) tracking method is used to: <br />
              identify the upper and lower boundaries of each stratum,  <br />
              determine the inter-layer beam velocity and mean dielectric constant (ϵ) of the material in each stratum, <br />
              identify the materials in the various strata from both the ϵs, known as molecular or atomic spectral lines (after fast Fourier transform (FFT) analysis of the received signals) and spectral ADR statistical parameters data based on known rock types. <br /><br />
              ADR accurately measures the dielectric permittivity of materials encountered and determines the ϵ of each layer of rock to an accuracy of at least 1:400.With deeper penetration and a narrower ADR beam, the accuracy of dielectric mapping improves, and at 1 km depth, an accuracy of 1:4000 can be achieved for the mean dielectrics of narrow layers at this depth. The ϵ is basically the effect that a given material has on slowing down the ADR transmission signal. Determining the ϵ of each layer enables each rock layer to be mapped with a depth computation from ground level and can also determine physical properties of the rock layer, such as moisture content, porosity, and density indices. In general, dielectric values for hydrocarbon layers in the Earth tend to be between 2 and 5 for the author's ADR scanner, if water is absent. In geological terms, the main effect on the signal's velocity as it propagates through the material is the water content. For example, air has a ϵ of 1, while water has a ϵ of 80. Most geological materials lie within these boundaries. <br /><br />
              Figure 2 shows the typical shape of an ADR transmit pulse into the Earth and the received energy from the Earth.  Figure 3 displays photographs of the ADR equipment.  </p>
