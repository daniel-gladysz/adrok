            <h2>4.  Conclusions</h2>
            <p>This poster has reported the subsurface Earth penetration capabilities of a deep-penetrating field spectrometer, known as ADR. Furthermore, the article has demonstrated the ability of ADR to recognize certain lithologic types (mainly coals) in real-world conditions. <br />
              The ADR scanner works by sending a narrow beam of energy into the ground using microwaves and radio waves. As it travels downward, the energy character of the beam is altered by the various rock layers it encounters. The beam is continually reflected back by these same rock layers and is recorded at surface. <br />
              The recorded data quantify how rocks and minerals, including hydrocarbons, interact with the beam as it passes through them and pinpoints their composition. The technology measures the dielectric permittivity of the subsurface as well as characterizing the nature of the rock types based on analysis of both the spectroscopic and resonant energy responses. <br />
              A key driver in pursuing development of ADR technology is to reduce the number of drill holes required to delineate a subsurface mineral or hydrocarbon reserve. To do this, laboratory analyses of rock specimens and data from training holes are used to guide the interpretation and analyses of the ADR results. As more samples are entered into Adrok's proprietary library, the confidence in the results will increase. <br />
              Another benefit to this approach is that ADR requires no land-use permitting as use of the technology offers a non-destructive, environmentally friendly way of remotely deducing subsurface geology. <br />
              Since 2007, this ADR methodology has been successfully deployed as a geophysical service by Adrok in the exploration and appraisal of subsurface geological structures and targets (platinum group metals, zinc, nickel, copper, massive sulphides, uranium, and hydrocarbon deposits). <br />
              Further research and testing of the ADR system is being conducted to test the depth limits to which the ADR can penetrate as well as its reliability and repeatability of material classification (spectroscopic capabilities).</p>  
