            <h3>2.2. Producing the ADR beam for remote sensing</h3>
            <p>ADR transmitting antennas are optically designed using dielectric lenses and can be referred to as behaving like radio wave and microwave telescopes.  In the ADR transmitting telescopes, the dielectric lens sequences amplify the pulsed waves, but the optical feedback provided by lasing in the telescope chamber converts the system into an oscillator and then into a coherent, invisible light generator. (By invisible light, we mean frequencies within the radio wave and microwave spectrum.) <br /><br />
              An ADR beam transmitted through the ground is a pulsed, confocal beam (like a long, narrow inverted cone in shape) of coherent (in-phase) radio waves and microwaves, producing minimal dispersion through its confocal and resonant mased nature. The transmitted ADR beams have two components: a long wavefront standing wave to achieve deep penetration and shorter resonance waves within the standing wave to enhance vertical resolution. The transmitted ADR beams typically operate within the frequency range of 1 – 100MHz.</p>
            <h3>2.3. ADR receiving system</h3>
            <p>The ADR transmitters must be oriented in the same direction as the receiving antennas. The receiving antenna (Rx) is kept vertical pointing into the ground at 90° look angle and the transmitting antenna (Tx) is thus moved away from Rx along the x-direction looking at 90° into the ground. The increasing beam dispersion through ray path move-outs can be computed and the increasing cylindrical beam widths are computed and plotted as the X-separation distance increases. By simple triangulation, the changing beam velocities through rock layers of differing dielectrics (ϵ) can be computed by normal move-out (NMO) mathematics and ray tracing theory (after Snell's law). Since the transmitting beam set-up is lased in the cylindrical chamber of the transmitting telescope and the speed of the lased beam is slowed down by special dielectric optics in the chamber, the resulting beam dispersion going down through the ground becomes slightly narrower with depth. This arrangement is called confocal focusing at rock layers and is ideal for mapping geological layers of varying ϵs, in each case plotting the exact rock volume illuminated between layer-bounded beam limits. </p>
            <h3>2.4. Processing and interpretation of the received ADR signals</h3>
            <p>The analysis of the returned ADR signals from the subsurface to the ADR receiving system is performed by FFT analysis of the received spectrum (which includes radio waves and microwaves). This results in mathematical and statistical analyses of the received spectrum and the recognition of energy, frequency, and phase relationships. <br /><br />
              More detailed spectrometric analysis is achieved through the quantum electrodynamic (QED) approach (Feynman 1985) by quantizing the entire ADR receive spectrum. In ADR quantum theory, this is similar to the equal temperament system of tuning musical instruments – in which each pair of adjacent musical notes has an identical frequency ratio. In this tuning methodology, an interval (usually an octave) is divided into a series of equal steps, with equal frequency ratios between successive notes. For example, a violin is tuned in perfect fifths. The notes are G–D–A–E, where the fundamental G is 192 Hz, D is 288 Hz, A is 432 Hz, and E is 648 Hz. Note that a perfect fifth is in a 3:2 relationship with its base tone. The octave frequency interval is covered in seven steps (A, B, C, D, E, F, and G) and each tone is the ratio of the seventh root of 3/2, or in mathematical form ((3/2)1/7). For ADR, the quantized frequency spectrum includes 3 octaves of the radio wave spectrum and 33 octaves of the microwave spectrum. When ADR is applied to geological analysis, rocks can be genetically classified by notes and octaves using the above spectrometric approach – adding a new quantum dimension to recording rock music.</p>
