            <h2>Our scanner works with a unique combination of physics, mathematics and technical innovation.</h2>
            <p>Dr Colin Stove, the founder and inspiration behind the Adrok ADR Scanner, had a distinguished career in the field of radar – working with radar sensors from both satellites and the Space Shuttle, as well as working extensively with ground penetrating radar. <br /><br />
            This fundamental understanding of wave and light physics gave him the insight and knowledge he needed to successfully reinvent the subsurface scanner using an advanced ‘coherent’ beam (two waves working together), that has worked so effectively, it is fundamentally changing the way the exploration industry works.</p>
           
            <h3>Why Atomic Dielectric Resonance?</h3>
            <p>The first three letters of our name A. D. R. stand for ‘Atomic Dielectric Resonance’. And this phrase is the key to how the scanner works. This is what we create and what we interpret. At an atomic level we measure ‘dielectric resonance’, which is the unique way elements resonate when hit by electromagnetic waves.</p>
            
            <h3>How does the scanner do what other scanners can’t?</h3>
            <p>The Adrok scanner transmits and then receives back, narrow pulsed beams of micro and radio waves. When these waves cross a boundary between element types, an ‘echo’ is reflected back to the scanner. <br /><br />
            But the ‘echo’ that is received is different to the one that was sent out. It has a slightly different resonance.  Why? Because an element has its own Dielectric Permittivity – or ‘resistance’ to the electromagnetic wave that we hit it with - think of it like pouring water on a variety of household objects - all will absorb and repel  the water in a different and unique way depending on what it is made of. <br /><br />
            This ‘echo’ is as unique as a fingerprint, and can then be analyzed to reveal detailed information on the characteristics of the element that has been discovered. <br /><br />
            Because the micro and radio waves are both absorbed and reflected by a material, we can also obtain  accurate measurements of the shape and depth of the element scanned (effectively a map).</p>
           
            <h2>Our ability to identify and map resources has been the true breakthrough</h2>
            <p>After obtaining subsurface data received by the scan, we positively identify the substances using two methods: spectroscopy (this turns our wavelength data into a decipherable spectrum), and Dielectrics - where the readings we obtain are compared with our existing table of substance classification (for example, water has a reading of 80, whereas air has a reading of 1).
            In addition to classification, the readings can also analyse fluid content and physical characteristic facts - like well locks and rock petrography.
            We can also map the length and depth of the substance to an unrivalled accuracy. Because our  electromagnetic waves reflect AND absorb we can potentially measure its three dimensional shape and capacity as well as an exact position. So - no more guesswork! <br /><br />
            High quality mapping is probably the most important advantages to the Adrok scanner. The more you know about your resource the more you can effectively extract it. Within the oil and gas and mining sectors, substances can now be quantified before extraction. Our technology can also be used to develop  further new reserves as well as monitor existing producing resources. <br /><br />
            The Adrok scanner can identify liquids - like hydrocarbons or water, and minerals - like Gold, Copper and Uranium, and the scanner is portable enough to be used in all terrains and transport – be it desert, jungle, mountain – on a boat, plane, helicopter or truck.</p>
            
